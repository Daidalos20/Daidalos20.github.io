// TIMIER !!!!!
// initilize timer
let timer_running = false;
var timer_time_ms = 0;
let intervalId = null;

function updateTimer() {
  let seconds = timer_time_ms / 1000; // divide by 10 to get 2 decimal places
  let timerElement = document.getElementById('timer');
  timerElement.textContent = `Gesammt Zeit ${seconds.toFixed(2)}s`; // display 2 decimal places
}

function startTimer() {
  if (!timer_running) {
    timer_running = true;
    intervalId = setInterval(() => {
      timer_time_ms+=100;
      updateTimer();
    }, 100); // update every 100ms to get 1 decimal place
  }
}

function pauseTimer() {
  if (timer_running) {
    clearInterval(intervalId);
    timer_running = false;
  }
}

function unpauseTimer() {
    startTimer();
}

function resetTimer() {
  pauseTimer();
  timer_time_ms = 0;
  updateTimer();
}

// NAV OVERLAY
function showNav() {
  document.querySelector('.nav-icon').classList.add('active');
  const navOverlay = document.querySelector('.nav-overlay');
  navOverlay.classList.add('show');
  navOverlay.style.animation = 'slideIn 0.5s forwards';
}

function hideNav() {
  const navOverlay = document.querySelector('.nav-overlay');
  if (navOverlay.classList.contains('show')) {
    document.querySelector('.nav-icon').classList.remove('active');
    navOverlay.classList.remove('show');
    navOverlay.style.animation = 'slideOut 0.5s forwards';
  }
}

// NAV OVERLAY
function toggleNav() {
  const navOverlay = document.querySelector('.nav-overlay');

  if (navOverlay.classList.contains('show')) {
    hideNav();
  } else {
    showNav();
  }
}


const navOverlay = document.querySelector('.nav-overlay');

let navOverlayButtons = [];

function loadNavOverlay() {
  Object.keys(item_containers).forEach((key) => {
    const button = document.createElement('button');
    button.id = key;
    button.textContent = item_containers[key]["meta"]["display_name"];
    button.onclick = function() {
      startNewRound(this.id);
    };
    navOverlayButtons.push(button); // store the buttons in an array
    navOverlay.appendChild(button);
  });

  updateHighlighting(); // call the updateHighlighting function initially to set the highlighting
}

function updateHighlighting() {
  // remove the highlighted class from all buttons
  navOverlayButtons.forEach((button) => {
    button.classList.remove('highlighted');
  });

  // add the highlighted class to the button with the matching id
  const highlightedButton = document.getElementById(current_container_name);
  if (highlightedButton) {
    highlightedButton.classList.add('highlighted');
  }
}


function startNewRound(container_name){
  hideLocationHint();
  loadContainer(container_name);
  next_stop_button_element.innerHTML = `Neue Runde mit ${item_containers[container_name]["meta"]["display_name"]} starten!`;
  itemElement.innerHTML = "hier wird der Gesuchte gegenstand angezeigt"
  updateHighlighting();
  hideNav();
  resetTimer();
} 

function loadContainer(container_name) {
  items_left = shuffleList(item_containers[container_name]["items"]);
  current_container_name = container_name;
  update_progress_bar()
}

function shuffleList(list) {
  // Create a copy of the original list to avoid modifying it
  const shuffledList = [...list];
  // Use the Fisher-Yates shuffle algorithm to shuffle the list
  for (let i = shuffledList.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffledList[i], shuffledList[j]] = [shuffledList[j], shuffledList[i]];
  }
  return shuffledList;
}

function start_stop_timer() {
  if (!timer_running) {
    //next item
    if (items_left.length === 0) {
      startNewRound(current_container_name)
    } else {
      hideLocationHint();
      show_next_item();
      next_stop_button_element.innerHTML = "Gefunden!";
      startTimer();
    }
  } else {
    // item found
    update_progress_bar()
    pauseTimer();
    if (items_left.length === 0){
      show_results();
    } else {
      next_stop_button_element.innerHTML = "Nächster Gegenstand";
    }
  }
}

function show_results() {
  itemElement.innerHTML = "Alle Gegenstände zugeordnet";
  
  const nextStopButtonElement = document.getElementById("next_stop_button");
  nextStopButtonElement.innerHTML = `Erneute Runde mit ${current_container_name} Starten
?</br>Alternativ:Oben rechts können andere Orte beübt werden.`;
}

function update_progress_bar(){
  const progressBarElement = document.getElementById("progress-bar");
  const progressTextElement = document.getElementById("progress-text");
  const items_done = (item_containers[current_container_name]["items"].length -items_left.length)
  const progress = (items_done / item_containers[current_container_name]["items"].length) * 100;
  progressBarElement.style.width = `${progress}%`;
  progressTextElement.textContent = `${items_done} von ${item_containers[current_container_name]["items"].length}`;
}

function show_next_item() {
  let item = items_left.pop();
  const itemElement = document.getElementById("item");
  itemElement.innerHTML = item.name;
  // Set location hint
  const locationHintElement = document.getElementById("location-hint");
  locationHintElement.innerHTML = item.location;

}

